import java.util.*;

public class switch {
    public static void main (String args[]){

        int number = 2;

        switch (number){
            case 1 : 
            System.out.println("Samosa");
            break;

            case 2 :
             System.out.println("birger");
            break;

            case 3 :
             System.out.println("mango");

            default :
             System.out.println("nothing");
        }
    }
}
